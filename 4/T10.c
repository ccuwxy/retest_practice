#include<stdio.h>

int main()
{
	double i, r1, r2, r3, r4, r5, r6, b;
	r1 = 0.1;
	r2 = 0.075;
	r3 = 0.05;
	r4 = 0.03;
	r5 = 0.015;
	r6 = 0.01;
	scanf("%lf", &i);
	if (i <= 100000)
		b = i * r1;
	else if (i <= 200000)
		b = 100000 * r1 + (i - 100000) * r2;
	else if (i <= 400000)
		b = 100000 * r1 + 100000 * r2 + (i - 200000) * r3;
	else if (i <= 600000)
		b = 100000 * r1 + 100000 * r2 + 200000 * r3 + (i - 400000) * r4;
	else if (i <= 1000000)
		b = 100000 * r1 + 100000 * r2 + 200000 * r3 + 200000 * r4 + (i - 600000) * r5;
	else
		b = 100000 * r1 + 100000 * r2 + 200000 * r3 + 200000 * r4 + 400000 * r5 + (i - 1000000) * r6;
	printf("%.2lf", b);
	return 0;
}